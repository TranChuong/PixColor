    func flippedHorizontally() -> UIImage {
        return UIImage(cgImage: self.cgImage!, scale: CGFloat(1.0), orientation: .upMirrored)
    }

    func flippedVertically() -> UIImage {
        return UIImage(cgImage: self.cgImage!, scale: CGFloat(1.0), orientation: .leftMirrored)
    }

    func getPixelColor(pos: CGPoint) -> UIColor {
         
        let pixelData = self.cgImage!.dataProvider!.data
        let data: UnsafePointer<UInt8> = CFDataGetBytePtr(pixelData)
        
        let width: Int = self.cgImage!.width

        let pixelInfo: Int = ((Int(width) * Int(pos.y)) + Int(pos.x)) * 4
        
        let r = CGFloat(data[pixelInfo]) / CGFloat(255.0)
        let g = CGFloat(data[pixelInfo+1]) / CGFloat(255.0)
        let b = CGFloat(data[pixelInfo+2]) / CGFloat(255.0)
        let a = CGFloat(data[pixelInfo+3]) / CGFloat(255.0)
        
        return UIColor(red: r, green: g, blue: b, alpha: a)
    }
    
